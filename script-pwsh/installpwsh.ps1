# Digite o comando a seguir para determinar a versão do PowerShell instalada.

$PSVersionTable.PSVersion

# Se o número de versão principal for inferior à 7, siga estas instruções https://learn.microsoft.com/pt-br/powershell/scripting/install/installing-powershell-on-windows?view=powershell-7.3

# Você precisa ter o SDK do .NET instalado para executar este comando.

dotnet tool install --global PowerShell